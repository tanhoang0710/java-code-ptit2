    private void cbSapXepItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbSapXepItemStateChanged
        if(cbSapXep.getSelectedIndex() == 1) {
            lBDT.sort(new Comparator<BDT>() {
                @Override
                public int compare(BDT o1, BDT o2) {
                    return o1.getTg().compareToIgnoreCase(o2.getTg());
                }
            });
        }
        if(cbSapXep.getSelectedIndex() == 2) {
            lBDT.sort(new Comparator<BDT>() {
                @Override
                public int compare(BDT o1, BDT o2) {
                    return Integer.compare(o1.getTr().getDongia(), o2.getTr().getDongia());
                }
            });
        }
        tmBDT.setRowCount(0);
        for (BDT x : lBDT) {
            tmBDT.addRow(x.toObject());
        }
    }//GEN-LAST:event_cbSapXepItemStateChanged

