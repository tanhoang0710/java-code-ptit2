    private void loadTR() {
        File f = new File(fTR);
        if(f.exists()) {
            lTR = IOFile.doc(fTR);
        }else {
            lTR = new ArrayList<>();
        }
    }
    
    private void loadKH() {
        File f = new File(fKH);
        if(f.exists()) {
            lKH = IOFile.doc(fKH);
        }else {
            lKH = new ArrayList<>();
        }
    }
    
    private void loadBDT() {
        File f = new File(fBDT);
        if(f.exists()) {
            lBDT = IOFile.doc(fBDT);
        }else {
            lBDT = new ArrayList<>();
        }
    }
    
    private void showTR(List<Tour> l) {
        tmTR.setRowCount(0);
        for (Tour x : l) {
           tmTR.addRow(x.toObject());
        }
    }
    
    private void showKH(List<KhachHang> l) {
        tmKH.setRowCount(0);
        for (KhachHang x : l) {
           tmKH.addRow(x.toObject());
        }
    }
    
    private void showBDT(List<BDT> l) {
        tmBDT.setRowCount(0);
        for (BDT x : l) {
           tmBDT.addRow(x.toObject());
        }
    }
    
    private void load2Ma() {
        cbMaKH.removeAllItems();
        cbMaTour.removeAllItems();
        for (Tour x : lTR) {
            cbMaTour.addItem(x.getMa() + "");
        }
        for (KhachHang x : lKH) {
            cbMaKH.addItem(x.getMa() + "");
        }
    }
    
    private boolean checkTrung(int maKH, int maTR, String ngayBD) {
        for (BDT x : lBDT) {
            if(x.getKh().getMa() == maKH && x.getTr().getMa() == maTR && x.getTg().equals(ngayBD))
                return true;
        }
        return false;
    }
    
    private Tour getTour(int ma) {
        for (Tour x : lTR) {
            if(x.getMa() == ma)
                return x;
        }
        return new Tour();
    }
    
    private KhachHang getKH(int ma) {
        for (KhachHang x : lKH) {
            if(x.getMa() == ma)
                return x;
        }
        return new KhachHang();
    }
