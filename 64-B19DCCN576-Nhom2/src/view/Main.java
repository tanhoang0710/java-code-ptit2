    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int r = jTable2.getSelectedRow();
        if(r >= 0 && r < jTable2.getRowCount()){
            String kieu, ten;
            int ma;
            try {
                ma = Integer.parseInt(jTextField5.getText());
                kieu = jComboBox2.getSelectedItem().toString();
                ten = jTextField6.getText();
                KhachHang x = new KhachHang(ma, ten, kieu);
                tmKH.setValueAt(ma, r, 0);
                tmKH.setValueAt(ten, r, 1);
                tmKH.setValueAt(kieu, r, 2);
                lKH.set(r, x);
            } catch (TrongException e) {
                JOptionPane.showMessageDialog(this, "Ko de trong");
                return;
            }
        }else {
            JOptionPane.showMessageDialog(this, "Chon khach hang de sua");
            return;
        }
    }//GEN-LAST:event_jButton5ActionPerformed

