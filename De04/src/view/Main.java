    private void loadMH() {
        File f = new File(fMH);
        if(f.exists()) {
            listMH = IOFile.doc(fMH);
        }else {
            listMH = new ArrayList<>();
        }
    }
    
    private void loadNV() {
        File f = new File(fNV);
        if(f.exists()) {
            listNV = IOFile.doc(fNV);
        }else {
            listNV = new ArrayList<>();
        }
    }
    
    private void loadDSBH() {
        File f = new File(fDSBH);
        if(f.exists()) {
            listDSBH = IOFile.doc(fDSBH);
        }else {
            listDSBH = new ArrayList<>();
        }
    }
    
    private void showMH(List<MatHang> list) {
        tmMH.setRowCount(0);
        for (MatHang x : list) {
            tmMH.addRow(x.toObject());
        }
    }
    
    private void showNV(List<NhanVien> list) {
        tmNV.setRowCount(0);
        for (NhanVien x : list) {
            tmNV.addRow(x.toObject());
        }
    }
    
    private void showDSBH(List<DSBH> list) {
        tmDSBH.setRowCount(0);
        for (DSBH x : list) {
            tmDSBH.addRow(x.toObject());
        }
    }
    
    private boolean trung2Ma(int maMH, int maNV) {
        for (DSBH x : listDSBH) {
            if(x.getMh().getMa() == maMH && x.getNv().getMa() == maNV)
                return true;
        }
        return false;
    }
    
    private int demMH(int maNV) {
        int count  = 0;
        for (DSBH x : listDSBH) {
            if(x.getNv().getMa() == maNV)
                count++;
        }
        return count;
    }
    
    private void load2Ma() {
        cbMaMH.removeAllItems();
        cbMaNV.removeAllItems();
        for (MatHang x : listMH) {
            cbMaMH.addItem(x.getMa() + "");
        }
        for (NhanVien x : listNV) {
            cbMaNV.addItem(x.getMa() + "");
        }
    }
    
    public MatHang getMatHang(int ma) {
        for (MatHang x : listMH) {
            if(x.getMa() == ma) 
                return x;
        }
        return new MatHang();
    }
    
    private NhanVien getNhanVien(int ma) {
        for (NhanVien x : listNV) {
            if(x.getMa() == ma) 
                return x;
        }
        return new NhanVien();
    }

