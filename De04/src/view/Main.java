    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        int n = listMH.size();
        if(n > 0){
            MatHang.setSma(listMH.get(n - 1).getMa() + 1);
        }
        jTextField1.setText(MatHang.getSma() + "");
        String ten, nhom;
        double gia;
        int sl;
        try {
            ten = jTextField2.getText();
            nhom = jComboBox1.getSelectedItem().toString();
            gia = Double.parseDouble(jTextField3.getText());
            sl = Integer.parseInt(jTextField4.getText());
            MatHang mh = new MatHang(ten, nhom, gia, sl);
            tmMH.addRow(mh.toObject());
            listMH.add(mh);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Nhap so");
            return;
        } catch (TrongException ex) {
            JOptionPane.showMessageDialog(this, "Ko de trong");
            return;
        } catch (SoException ex) {
            JOptionPane.showMessageDialog(this, "Nhap so");
            return;        
        }
    }//GEN-LAST:event_btThemActionPerformed

