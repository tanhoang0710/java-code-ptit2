    private void loadNV() {
        File f = new File(fNV);
        if(f.exists()) {
            listNV = IOFile.doc(fNV);
        }else {
            listNV = new ArrayList<>();
        }
    }
    
    private void loadPB() {
        File f = new File(fPB);
        if(f.exists()) {
            listPB = IOFile.doc(fPB);
        }else {
            listPB = new ArrayList<>();
        }
    }
    
    private void loadDSCC() {
        File f = new File(fDSCC);
        if(f.exists()) {
            listDSCC = IOFile.doc(fDSCC);
        }else {
            listDSCC = new ArrayList<>();
        }
    }
    
    private void showNV(List<NhanVien> list) {
        tmNV.setRowCount(0);
        for (NhanVien x : list) {
            tmNV.addRow(x.toObject());
        }
    }
    
    private void showPB(List<PhongBan> list) {
        tmPB.setRowCount(0);
        for (PhongBan x : list) {
            tmPB.addRow(x.toObject());
        }
    }
    
    private void showDSCC(List<DSCC> list) {
        tmDSCC.setRowCount(0);
        for (DSCC x : list) {
            tmDSCC.addRow(x.toObject());
        }
    }
    
    private void load2Ma() {
        cbMaNV.removeAllItems();
        cbMaPB.removeAllItems();
        for (NhanVien x : listNV) {
            cbMaNV.addItem(x.getMa() + "");
        }
        for (PhongBan x : listPB) {
            cbMaPB.addItem(x.getMa() + "");
        }
    }
    
    private boolean checkTonTai(int maPB, int maNV) {
        for (DSCC x : listDSCC) {
            if(x.getNv().getMa() == maNV && x.getPb().getMa() == maPB) 
                return true;
        }
        return false;
    }
    
    private NhanVien getNhanVien(int ma) {
        for (NhanVien x : listNV) {
            if(x.getMa() == ma)
                return x;
        }
        return new NhanVien();
    }
    
    private PhongBan getPhongBan(int ma) {
        for (PhongBan x : listPB) {
            if(x.getMa() == ma)
                return x;
        }
        return new PhongBan();
    }

