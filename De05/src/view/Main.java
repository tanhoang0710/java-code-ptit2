    private void cbSapXepItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbSapXepItemStateChanged
        if(cbSapXep.getSelectedIndex()== 1) {
            listDSCC.sort(new Comparator<DSCC>() {
                @Override
                public int compare(DSCC o1, DSCC o2) {
                    String ten1[] = o1.getNv().getHoten().split("\\s+");
                    String ten2[] = o2.getNv().getHoten().split("\\s+");
                    if(ten1[ten1.length - 1].equalsIgnoreCase(ten2[ten2.length - 1])){
                        return o1.getNv().getHoten().compareToIgnoreCase(o2.getNv().getHoten());
                    }else return ten1[ten1.length - 1].compareToIgnoreCase(ten2[ten2.length - 1]);
                }
            });
        }
        
        if(cbSapXep.getSelectedIndex()== 2) {
            listDSCC.sort(new Comparator<DSCC>() {
                @Override
                public int compare(DSCC o1, DSCC o2) {
                    return Integer.compare(o1.getPb().getMa(), o2.getPb().getMa());
                }
            });
        }
        tmDSCC.setRowCount(0);
        for (DSCC x : listDSCC) {
            tmDSCC.addRow(x.toObject());
        }
    }//GEN-LAST:event_cbSapXepItemStateChanged

