    private void cbSapXepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbSapXepActionPerformed
        if(cbSapXep.getSelectedIndex() == 1) {
            lQLTB.sort(new Comparator<QLTB>() {
                @Override
                public int compare(QLTB o1, QLTB o2) {
                    String ten1[] = o1.getPh().getTen().split("\\s+");
                    String ten2[] = o2.getPh().getTen().split("\\s+");
                    if(ten1[ten1.length - 1].equalsIgnoreCase(ten2[ten2.length - 1])){
                        return o1.getPh().getTen().compareToIgnoreCase(o2.getPh().getTen());
                    }else return ten1[ten1.length - 1].compareToIgnoreCase(ten2[ten2.length - 1]);
                }
            });
        }
        
        if(cbSapXep.getSelectedIndex() == 2) {
            lQLTB.sort(new Comparator<QLTB>() {
                @Override
                public int compare(QLTB o1, QLTB o2) {
                    return Integer.compare(o1.getSl(), o2.getSl());
                }
            });
        }
        tmQLTB.setRowCount(0);
        for (QLTB x : lQLTB) {
            tmQLTB.addRow(x.toObject());
        }
    }//GEN-LAST:event_cbSapXepActionPerformed

