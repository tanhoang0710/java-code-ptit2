    private void load2Ma() {
        cbMaPH.removeAllItems();
        cbMaTB.removeAllItems();
        for (PhongHoc x : lPH) {
            cbMaPH.addItem(x.getMa() + "");
        }
        
        for (ThietBi x : lTB) {
            cbMaTB.addItem(x.getMa() + "");
        }
    }
    
    private boolean trung2ma(int maTB, int maPH) {
        for (QLTB x : lQLTB) {
            if(x.getPh().getMa() == maPH && x.getTb().getMa() == maTB)
                return true;
        }
        return false;
    }
    
    public ThietBi getTb(int ma) {
        for (ThietBi x : lTB) {
            if(x.getMa() == ma)
                return x;
        }
        return new ThietBi();
    }
    
    public PhongHoc getPh(int ma) {
        for (PhongHoc x : lPH) {
            if(x.getMa() == ma)
                return x;
        }
        return new PhongHoc();
    }

