    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        int maPH = Integer.parseInt(cbMaPH.getSelectedItem().toString());
        int maLH = Integer.parseInt(cbMaLH.getSelectedItem().toString());
        int kip;
        try {
            kip = Integer.parseInt(jTextField11.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Nhap so");
            return;
        }
        if(checkTrung(maPH, maLH, kip)){
            JOptionPane.showMessageDialog(this, "Da trung");
            return;
        }
        PhongHoc x = getPhongHoc(maPH);
        Lop y = getLop(maLH);
        if(x.getSoGhe() < y.getSoSV()) {
            JOptionPane.showMessageDialog(this, "Phong ko du ghe");
            return;
        }
        BSX bsx = new BSX(y, x, kip);
        tmBSX.addRow(bsx.toObject());
        lBSX.add(bsx);
    }//GEN-LAST:event_jButton9ActionPerformed

    private void cbSapXepItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbSapXepItemStateChanged
        if(cbSapXep.getSelectedIndex() == 1) {
            lBSX.sort(new Comparator<BSX>() {
                @Override
                public int compare(BSX o1, BSX o2) {
                    return o1.getL().getTen().compareToIgnoreCase(o2.getL().getTen());
                }
            });
        }
        if(cbSapXep.getSelectedIndex() == 2) {
            lBSX.sort(new Comparator<BSX>() {
                @Override
                public int compare(BSX o1, BSX o2) {
                    return Integer.compare(o1.getKip(), o2.getKip());
                }
            });
        }
        tmBSX.setRowCount(0);
        for (BSX x : lBSX) {
            tmBSX.addRow(x.toObject());
        }
    }//GEN-LAST:event_cbSapXepItemStateChanged

