    private void loadPH() {
        File f = new File(fPH);
        if(f.exists()) {
            lPH = IOFile.doc(fPH);
        }
        else {
            lPH = new ArrayList<>();
        }
    }
    
    private void loadLH() {
        File f = new File(fLH);
        if(f.exists()) {
            lL = IOFile.doc(fLH);
        }
        else {
            lL = new ArrayList<>();
        }
    }
    
    private void loadBSX() {
        File f = new File(fBSX);
        if(f.exists()) {
            lBSX = IOFile.doc(fBSX);
        }
        else {
            lBSX = new ArrayList<>();
        }
    }
    
    private void showPH(List<PhongHoc> l) {
        tmPH.setRowCount(0);
        for (PhongHoc x : l) {
            tmPH.addRow(x.toObject());
        }
    }
    
    private void showLH(List<Lop> l) {
        tmLH.setRowCount(0);
        for (Lop x : l) {
            tmLH.addRow(x.toObject());
        }
    }
    
    private void showBSX(List<BSX> l) {
        tmBSX.setRowCount(0);
        for (BSX x : l) {
            tmBSX.addRow(x.toObject());
        }
    }
    
    private void load2Ma() {
        cbMaLH.removeAllItems();
        cbMaPH.removeAllItems();
        for (PhongHoc x : lPH) {
            cbMaPH.addItem(x.getMa() + "");
        }
        for (Lop x : lL) {
            cbMaLH.addItem(x.getMa() + "");
        }
    }
    
    private boolean checkTrung(int maPH, int maLH, int kip) {
        for (BSX x : lBSX) {
            if(x.getL().getMa() == maPH && x.getPh().getMa() == maPH && x.getKip() == kip) 
                return true;
        }
        return false;
    }
    
    public PhongHoc getPhongHoc(int ma) {
        for (PhongHoc x : lPH) {
            if(x.getMa() == ma) 
                return x;
        }
        return new PhongHoc();
    }
    
    public Lop getLop(int ma) {
        for (Lop x : lL) {
            if(x.getMa() == ma) 
                return x;
        }
        return new Lop();
    }

