    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        int maGV = Integer.parseInt(cbMaGV.getSelectedItem().toString());
        int maMH = Integer.parseInt(cbMaMH.getSelectedItem().toString());
        if(checkTrung(maGV, maMH)){
            JOptionPane.showMessageDialog(this, "Da ton tai");
            return;
        }
        int soLop;
        try {
            soLop = Integer.parseInt(jTextField12.getText());
            GiangVien gv = getGiangVien(maGV);
            MonHoc mh = getMonHoc(maMH);
            gv.setSoTietMax(gv.getSoTietMax() - mh.getTongSoTiet());
            if(gv.getSoTietMax() <= 0){
                JOptionPane.showMessageDialog(this, "Da het tiet");
                return;
            }
            KKGD kkgd = new KKGD(mh, gv, soLop);
            tmKKGD.addRow(kkgd.toObject());
            lKKGD.add(kkgd);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Nhap so");
            return;
        }
    }//GEN-LAST:event_jButton8ActionPerformed

