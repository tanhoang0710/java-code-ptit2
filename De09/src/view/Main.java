    private void cbSapXepItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbSapXepItemStateChanged
        if(cbSapXep.getSelectedIndex() == 1) {
            lKKGD.sort(new Comparator<KKGD>() {
                @Override
                public int compare(KKGD o1, KKGD o2) {
                    return o1.getGv().getTen().compareToIgnoreCase(o2.getGv().getTen());
                }
            });
        }
        
        if(cbSapXep.getSelectedIndex() == 2) {
            lKKGD.sort(new Comparator<KKGD>() {
                @Override
                public int compare(KKGD o1, KKGD o2) {
                    return Integer.compare(o1.getMh().getTongSoTiet(), o2.getMh().getTongSoTiet());
                }
            });
        }
        tmKKGD.setRowCount(0);
        for (KKGD x : lKKGD) {
            tmKKGD.addRow(x.toObject());
        }
    }//GEN-LAST:event_cbSapXepItemStateChanged

    private void jButton14ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton14ActionPerformed
        String key = jTextField13.getText().trim().toLowerCase();
        double tienLuong = 0, tong = 0;
        for (KKGD x : lKKGD) {
            tong = 0;
            if(x.getGv().getTen().contains(key)){
                tong = x.getSolop() * (x.getMh().getMucKP() * x.getMh().getSoTietLT() + x.getMh().getMucKP() * 0.7 * (x.getMh().getTongSoTiet() - x.getMh().getSoTietLT()));
                tienLuong += tong;
                GiangVien gv = getGiangVien(x.getGv().getMa());
                MonHoc mh = getMonHoc(x.getMh().getMa());
                QLT qlt = new QLT(mh, gv, tong);
                tmQLT.addRow(qlt.toObject());
                lQLT.add(qlt);
            }
        }
        jTextField10.setText(tienLuong + "");
    }//GEN-LAST:event_jButton14ActionPerformed

