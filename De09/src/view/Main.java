    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int n = lMH.size();
        if(n > 0) {
            MonHoc.setSma(lMH.get(n - 1).getMa() + 1);
        }
        jTextField1.setText(MonHoc.getSma() + "");
        String ten;
        int tongSoTiet, soTietLT;
        double tongKP;
        try {
            ten = jTextField2.getText();
            tongSoTiet = Integer.parseInt(jTextField3.getText());
            soTietLT = Integer.parseInt(jTextField4.getText());
            tongKP = Double.parseDouble(jTextField5.getText());
            MonHoc x= new MonHoc(ten, tongSoTiet, soTietLT, tongKP);
            tmMH.addRow(x.toObject());
            lMH.add(x);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Nhap so");
            jTextField3.requestFocus();
            return;
        } catch( TrongException e) {
            JOptionPane.showMessageDialog(this, "Ko de trong");
            jTextField2.requestFocus();
            return;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

