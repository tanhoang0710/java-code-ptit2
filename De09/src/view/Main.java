    private void showGV(List<GiangVien> l) {
        tmGV.setRowCount(0);
        for (GiangVien x : l) {
            tmGV.addRow(x.toObject());
        }
    }
    
    private void showMH(List<MonHoc> l) {
        tmMH.setRowCount(0);
        for (MonHoc x : l) {
            tmMH.addRow(x.toObject());
        }
    }
    
    private void showKKGD(List<KKGD> l) {
        tmKKGD.setRowCount(0);
        for (KKGD x : l) {
            tmKKGD.addRow(x.toObject());
        }
    }
    
    private void load2Ma() {
        cbMaGV.removeAllItems();
        cbMaMH.removeAllItems();
        for (GiangVien x : lGV) {
            cbMaGV.addItem(x.getMa() + "");
        }
        
        for (MonHoc x : lMH) {
            cbMaMH.addItem(x.getMa() + "");
        }
    }
    
    private boolean checkTrung(int maGV, int maMH) {
        for (KKGD x : lKKGD) {
            if(x.getGv().getMa() == maGV && x.getMh().getMa() == maMH)
                return true;
        }
        return false;
    }
    
    private MonHoc getMonHoc(int ma) {
        for (MonHoc x : lMH) {
            if(x.getMa() == ma)
                return x;
        }
        return new MonHoc();
    }
    
    private GiangVien getGiangVien(int ma) {
        for (GiangVien x : lGV) {
            if(x.getMa() == ma)
                return x;
        }
        return new GiangVien();
    }

