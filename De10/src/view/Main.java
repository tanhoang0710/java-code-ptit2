    private void loadCT() {
        File f = new File(fCT);
        if(f.exists()) {
            lCT = IOFile.doc(fCT);
        }else {
            lCT = new ArrayList<>();
        }
    }
    private void loadVT() {
        File f = new File(fVT);
        if(f.exists()) {
            lVT = IOFile.doc(fVT);
        }else {
            lVT = new ArrayList<>();
        }
    }
    private void loadBPC() {
        File f = new File(fBPC);
        if(f.exists()) {
            lBPC = IOFile.doc(fBPC);
        }else {
            lBPC = new ArrayList<>();
        }
    }
    
    private void showCT(List<CauThu> l) {
        tmCT.setRowCount(0);
        for (CauThu x : l) {
            tmCT.addRow(x.toObject());
        }
    }
    
    private void showVT(List<ViTri> l) {
        tmVT.setRowCount(0);
        for (ViTri x : l) {
            tmVT.addRow(x.toObject());
        }
    }
    
    private void showBPC(List<BPC> l) {
        tmBPC.setRowCount(0);
        for (BPC x : l) {
            tmBPC.addRow(x.toObject());
        }
    }
    
    private void load2Ma() {
        cbMaCT.removeAllItems();
        cbMaVT.removeAllItems();
        for (CauThu x : lCT) {
            cbMaCT.addItem(x.getMa()+"");
        }
        for (ViTri x : lVT) {
            cbMaVT.addItem(x.getMa()+"");
        }
    }
    
    
    private boolean trung2Ma(int maCT, int maVT){
        for (BPC x : lBPC) {
            if(x.getCt().getMa() == maCT && x.getVt().getMa() == maVT)
                return true;
        }
        return false;
    }
    
    private CauThu getCauThu(int ma) {
        for (CauThu x : lCT) {
            if(x.getMa() == ma)
                return x;
        }
        return new CauThu();
    }
    
    private ViTri getViTri(int ma){
        for (ViTri x : lVT) {
            if(x.getMa() == ma)
                return x;
        }
        return new ViTri();
    }

