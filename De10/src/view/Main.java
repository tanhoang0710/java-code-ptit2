    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        int maCT = Integer.parseInt(cbMaCT.getSelectedItem().toString());
        int maVT = Integer.parseInt(cbMaVT.getSelectedItem().toString());
        if(trung2Ma(maCT, maVT)){
            JOptionPane.showMessageDialog(this, "Da ton tai");
            return;
        }
        int soTran;
        try {
            soTran = Integer.parseInt(jTextField9.getText());
            CauThu x = getCauThu(maCT);
            x.setSoVTMax(x.getSoVTMax() - 1);
            if(x.getSoVTMax() <= 0){
                JOptionPane.showMessageDialog(this, "Da choi so vi tri toi da");
                return;
            }
            ViTri y = getViTri(maVT);
            BPC bpc = new BPC(x, y, soTran);
            tmBPC.addRow(bpc.toObject());
            lBPC.add(bpc);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Nhap so");
            return;
        }
    }//GEN-LAST:event_jButton8ActionPerformed

