    private void cbSapXepItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbSapXepItemStateChanged
        if(cbSapXep.getSelectedIndex() == 1) {
            lBPC.sort(new Comparator<BPC>() {
                @Override
                public int compare(BPC o1, BPC o2) {
                    return o1.getCt().getTen().compareToIgnoreCase(o2.getCt().getTen());
                }
            });
        }
        
        if(cbSapXep.getSelectedIndex() == 2) {
            lBPC.sort(new Comparator<BPC>() {
                @Override
                public int compare(BPC o1, BPC o2) {
                    return Integer.compare(o2.getSoTran(), o1.getSoTran());
                }
            });
        }
        tmBPC.setRowCount(0);
        for (BPC x : lBPC) {
            tmBPC.addRow(x.toObject());
        }
    }//GEN-LAST:event_cbSapXepItemStateChanged

    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        double tong = 0, tongThuong = 0;
        String key = jTextField10.getText().trim().toLowerCase();
        CauThu ct = new CauThu();
        for (BPC x : lBPC) {
            tong = 0;
            if(x.getCt().getTen().toLowerCase().contains(key)){
                tong = x.getVt().getMucthuong();
                tongThuong += tong;
                BT bt = new BT(x.getCt(), x.getVt(), (int) tong);
                ct = x.getCt();
                tmBT.setRowCount(0);
                tmBT.addRow(bt.toObject());
                lBT.add(bt);
            }
        }
        double res = tongThuong + 0.1 * ct.getMucluong();
        String s = res + "";
        jTextField11.setText(s);
    }//GEN-LAST:event_jButton12ActionPerformed

