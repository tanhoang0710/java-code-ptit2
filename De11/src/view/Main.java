    private void loadPH() {
        File f = new File(fPH);
        if(f.exists()) {
            lPH = IOFile.doc(fPH);
        }else {
            lPH = new ArrayList<>();
        }
    }
    private void loadKH() {
        File f = new File(fKH);
        if(f.exists()) {
            lKH = IOFile.doc(fKH);
        }else {
            lKH = new ArrayList<>();
        }
    }    
    private void loadBSX() {
        File f = new File(fBSX);
        if(f.exists()) {
            lBSX = IOFile.doc(fBSX);
        }else {
            lBSX = new ArrayList<>();
        }
    }
    
    private void shwoPH(List<Phong> l) {
        tmPH.setRowCount(0);
        for (Phong x : l) {
            tmPH.addRow(x.toObject());
        }
    }
    
    private void shwoKH(List<KhachHang> l) {
        tmKH.setRowCount(0);
        for (KhachHang x : l) {
            tmKH.addRow(x.toObject());
        }
    }
    
    private void shwoBSX(List<BSX> l) {
        tmBSX.setRowCount(0);
        for (BSX x : l) {
            tmBSX.addRow(x.toObject());
        }
    }
    
    private void load2Ma() {
        cbMaKH.removeAllItems();
        cbMaP.removeAllItems();
        for (Phong x : lPH) {
            cbMaP.addItem(x.getMa() + "");
        }
        for (KhachHang x : lKH) {
            cbMaKH.addItem(x.getMa() + "");
        }
    }
    
    private KhachHang getKhachHang(int ma) {
        for (KhachHang x : lKH) {
            if(x.getMa() == ma)
                return x;
        }
        return new KhachHang();
    }
    
    private Phong getPhong(int ma) {
        for (Phong x : lPH) {
            if(x.getMa() == ma) 
                return x;
            
        }
        return new Phong();
    }
