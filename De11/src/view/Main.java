    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        String key = jTextField10.getText().trim().toLowerCase();
        tmBHD.setRowCount(0);
        double tong = 0, hoadon = 0;
        for (BSX x : lBSX) {
            if(x.getKh().getTen().toLowerCase().contains(key)){
                tong = x.getKh().getSophongcanthue() * x.getP().getMuctienthue() * x.getSongay();
                hoadon += tong;
                KhachHang kh= getKhachHang(x.getKh().getMa());
                Phong ph = getPhong(x.getP().getMa());
                BHD xx = new BHD(ph, kh, tong);
                tmBHD.addRow(xx.toObject());
                lBHD.add(xx);
            }
        }
        jTextField11.setText(hoadon + "");
    }//GEN-LAST:event_jButton12ActionPerformed

