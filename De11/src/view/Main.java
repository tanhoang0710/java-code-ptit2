    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        int maPH = Integer.parseInt(cbMaP.getSelectedItem().toString());
        int maKH = Integer.parseInt(cbMaKH.getSelectedItem().toString());
        int sn;
        try {
            sn = Integer.parseInt(jTextField9.getText());
            KhachHang x = getKhachHang(maKH);
            Phong y = getPhong(maPH);
            if(x.getSophongcanthue() > y.getSophong()) {
                JOptionPane.showMessageDialog(this, "Loai phong nay da het");
                return;
            }
            y.setSophong(y.getSophong() - x.getSophongcanthue());
            BSX bsx = new BSX(y, x, sn);
            tmBSX.addRow(bsx.toObject());
            lBSX.add(bsx);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Nhap so");
            return;
        }
    }//GEN-LAST:event_jButton8ActionPerformed

