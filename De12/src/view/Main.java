    private void loadCN() {
        File f = new File(fCN);
        if(f.exists())
            lCN = IOFile.doc(fCN);
        else 
            lCN = new ArrayList<>();
    }
    
    private void loadSP() {
        File f = new File(fSP);
        if(f.exists())
            lSP = IOFile.doc(fSP);
        else 
            lSP = new ArrayList<>();
    }
    
    private void loadBTC() {
        File f = new File(fBTC);
        if(f.exists())
            lBTC = IOFile.doc(fBTC);
        else 
            lBTC = new ArrayList<>();
    }
    
    private void showCN(List<CongNhan> l) {
        tmCN.setRowCount(0);
        for (CongNhan x : l) {
            tmCN.addRow(x.toObject());
        }
    }
    
    private void showSP(List<SanPham> l) {
        tmSP.setRowCount(0);
        for (SanPham x : l) {
            tmSP.addRow(x.toObject());
        }
    }
    
    private void showBTC(List<BTC> l) {
        tmBTC.setRowCount(0);
        for (BTC x : l) {
            tmBTC.addRow(x.toObject());
        }
    }
    
    private void load2Ma() {
        cbMaCN.removeAllItems();
        cbMaSP.removeAllItems();
        for (CongNhan x : lCN) {
            cbMaCN.addItem(x.getMa() + "");
        }
        for (SanPham x : lSP) {
            cbMaSP.addItem(x.getMa() + "");
        }
    }
    
    private boolean check2Ma(int maCN, int maSP) {
        for (BTC x : lBTC) {
            if(x.getCn().getMa() == maCN && x.getSp().getMa() == maSP)
                return true;
        }
        return false;
    }
    
    private CongNhan getCongNhan(int ma) {
        for (CongNhan x : lCN) {
            if(x.getMa() == ma)
                return x;
        }
        return new CongNhan();
    }
    
    private SanPham getSanPham(int ma){
        for (SanPham x : lSP) {
            if(x.getMa() == ma)
                return x;
        }
        return new SanPham();
    }

