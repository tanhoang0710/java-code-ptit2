    private void cbSapXepItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbSapXepItemStateChanged
        if(cbSapXep.getSelectedIndex() == 1) {
            lBTC.sort(new Comparator<BTC>() {
                @Override
                public int compare(BTC o1, BTC o2) {
                    return o1.getCn().getTen().compareToIgnoreCase(o2.getCn().getTen());
                }
            });
        }
        if(cbSapXep.getSelectedIndex() == 2) {
            lBTC.sort(new Comparator<BTC>() {
                @Override
                public int compare(BTC o1, BTC o2) {
                    return Integer.compare(o2.getSl(), o1.getSl());
                }
            });
        }
        tmBTC.setRowCount(0);
        for (BTC x : lBTC) {
            tmBTC.addRow(x.toObject());
        }
    }//GEN-LAST:event_cbSapXepItemStateChanged

    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        String key = jTextField11.getText().trim().toLowerCase();
        double tong = 0, tongTN = 0;
        tmBTN.setRowCount(0);
        for (BTC x : lBTC) {
            tong = 0;
            if(x.getCn().getTen().contains(key)) {
                tong = x.getSp().getDongia() * x.getSl();
                tongTN += tong;
                CongNhan cn = getCongNhan(x.getCn().getMa());
                SanPham sp = getSanPham(x.getSp().getMa());
                BTN btn = new BTN(cn, sp, tong);
                tmBTN.addRow(btn.toObject());
                lBTN.add(btn);
            }
        }
        jTextField8.setText(tongTN + "");
    }//GEN-LAST:event_jButton9ActionPerformed

