    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        int maCN = Integer.parseInt(cbMaCN.getSelectedItem().toString());
        int maSP = Integer.parseInt(cbMaSP.getSelectedItem().toString());
        int sl;
        try {
            sl = Integer.parseInt(jTextField10.getText());
            CongNhan cn = getCongNhan(maCN);
            cn.setSoLoaiSPMax(cn.getSoLoaiSPMax() - 1);
            if(cn.getSoLoaiSPMax() < 0) {
                JOptionPane.showMessageDialog(this, "Cong nhan da het luot tham gia san xuat");
                return;
            }
            SanPham sp = getSanPham(maSP);
            BTC btc = new BTC(cn, sp, sl);
            tmBTC.addRow(btc.toObject());
            lBTC.add(btc);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Nhap so");
            return;
        }
    }//GEN-LAST:event_jButton8ActionPerformed

