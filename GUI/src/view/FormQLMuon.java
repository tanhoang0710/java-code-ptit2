    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int maBd = Integer.parseInt(cbMaBd.getSelectedItem().toString());
        int maS = Integer.parseInt(cbMaS.getSelectedItem().toString());
        if(trung2Ma(maBd, maS)){
            JOptionPane.showMessageDialog(this, "Da ton tai");
            cbMaBd.requestFocus();
            return;
        }
        int sl = 0;
        int slThuVien = getSachByMa(maS).getSl();
        try {
            sl = Integer.parseInt(jTextField1.getText());
            if(sl > slThuVien){
                JOptionPane.showMessageDialog(this, "Ko du sach");
                jTextField1.requestFocus();
                return;
            } 
            String ttrang = jTextField2.getText();
            QLMuon ql = new QLMuon(getBanDocByMa(maBd), getSachByMa(maS), sl, ttrang);
            tm.addRow(ql.toObject());
            lqlMuon.add(ql);
            getSachByMa(maS).setSl(slThuVien - sl);
            IOFile.viet(fsach, lsach);
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Nhap so");
            jTextField1.requestFocus();
            return;
        }
    }//GEN-LAST:event_jButton2ActionPerformed

