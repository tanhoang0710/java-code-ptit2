    private void load2Ma() {
        cbMaBd.removeAllItems();
        cbMaS.removeAllItems();
        String fsach = "src/controller/sach.txt";
        String fbandoc = "src/controller/bandoc.txt";
        List<BanDoc> l1 = IOFile.doc(fbandoc);
        if(l1 != null) {
            for (BanDoc x : l1) {
                cbMaBd.addItem(x.getMa()+"");
            }
        }
        for (Sach x : lsach) {
            cbMaS.addItem(x.getMa()+"");
        }
    }
    
    private void loadData() {
        File f = new File(fqlMuon);
        if(f.exists()){
            lqlMuon = IOFile.doc(fqlMuon);
        }else
            lqlMuon = new ArrayList<>();
        
        File fs = new File(fsach);
        if(fs.exists()){
            lsach = IOFile.doc(fsach);
        }else
            lsach = new ArrayList<>();
    }
    
    private boolean trung2Ma(int maBd, int maS) {
        for (QLMuon x : lqlMuon) {
            if(x.getBandoc().getMa() == maBd && x.getSach().getMa() == maS)
                return true;
        }
        return false;
    }
    
    private int demDauSach(int maBd) {
        int count = 0;
        for (QLMuon x : lqlMuon) {
            if(x.getBandoc().getMa() == maBd)
                count++;
        }
        return count;
    }
    
    private Sach getSachByMa(int ma) {
        for (Sach x : lsach) {
            if(x.getMa() == ma)
                return x;
        }
        return null;
    }
    
    private BanDoc getBanDocByMa(int ma) {
        String fbandoc= "src/controller/bandoc.txt";
        List<BanDoc> l1 = IOFile.doc(fbandoc);
        if(l1 != null) {
            for (BanDoc x : l1) {
                if(x.getMa() == ma)
                    return x;
            }
        }
        return null;
    }

